<template>
  <div :class="['AudioSeekbar', isLoaded ? 'is-loaded' : null]">
    <div class="AudioSeekbar__times">
      <div class="AudioSeekbar__current-time">
        {{ currentTimeText }}
      </div>
      <div class="AudioSeekbar__total-time">
        {{ totalTimeText }}
      </div>
    </div>
    <div
      class="AudioSeekbar__seeker"
      ref="seekbarRef"
      @mousemove="onSeekMouseMove"
      @mouseleave="onSeekMouseLeave"
      @click="onSeekbarClick"
    >
      <div
        class="AudioSeekbar__seek-progress"
        :style="{ width: `${currentTimePercent}%` }"
      ></div>

      <div
        v-if="displayPreview"
        class="AudioSeekbar__seek-preview"
        :style="{ width: `${previewPercent}%` }"
      ></div>

      <div
        v-if="displayPreview"
        class="AudioSeekbar__seek-preview-popup"
        :style="{ left: `${previewPercent}%` }"
      >
        {{ previewTimeText }}
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { computed, ref } from "vue";
import * as Utilities from "@/utilities";
import { assert } from "tsafe";

const UNLOADED_TIME_TEXT = "-- : --";

const props = withDefaults(
  defineProps<{
    songLength?: number;
    currentTime?: number;
  }>(),
  { currentTime: 0 }
);

const emit = defineEmits<{ (event: "seek", seekToTime: number): void }>();

const seekbarRef = ref<HTMLElement | null>(null);

const displayPreview = ref(false);
const previewPercent = ref(0);

const isLoaded = computed(() => props.songLength !== undefined);

const currentTimeText = computed(() => {
  if (!isLoaded.value) return UNLOADED_TIME_TEXT;
  return Utilities.displaySecondsString(props.currentTime);
});
const totalTimeText = computed(() => {
  if (!isLoaded.value) return UNLOADED_TIME_TEXT;
  assert(props.songLength !== undefined);
  return Utilities.displaySecondsString(props.songLength);
});
const previewTimeText = computed(() => {
  if (!isLoaded.value) return UNLOADED_TIME_TEXT;
  assert(props.songLength !== undefined);
  const previewTime = (props.songLength * previewPercent.value) / 100;
  return Utilities.displaySecondsString(previewTime);
});
const currentTimePercent = computed(() => {
  if (!isLoaded.value) return 0;
  assert(props.songLength !== undefined);
  return (props.currentTime / props.songLength) * 100;
});

const onSeekbarClick = (e: MouseEvent) => {
  const seekbarEl = seekbarRef.value;

  if (!isLoaded.value || !seekbarEl) return;
  assert(props.songLength !== undefined);

  const posPercent = e.offsetX / seekbarEl.offsetWidth;
  const seekToTime = props.songLength * posPercent;
  emit("seek", seekToTime);
};

const onSeekMouseMove = (e: MouseEvent) => {
  const seekbarEl = seekbarRef.value;

  if (!seekbarEl) return;

  const posPercent = (e.offsetX / seekbarEl.offsetWidth) * 100;

  displayPreview.value = true;
  previewPercent.value = posPercent;
};

const onSeekMouseLeave = () => {
  displayPreview.value = false;
  previewPercent.value = 0;
};
</script>

